                            <b>DIRECTION FOR USE </b><br>
                              <i>PROCEDURE FOR CONCRETE REPAIRS :</i> <br>
                            &nbsp;&nbsp;Before using ALBOND-BL40 ensure that the places of applications is cleaned thoroughly and made sound by removing all loose particles to sound concrete and also corroded steel bars. Clean the rusted steel bars with a wire brush or any other mechanical device.    <br>
                               &nbsp;&nbsp;Apply a thin coat on all the reinforcements and concrete to be patched by diluting 1 part ALBOND-BL40 with 2 part of  water by volume and mixed with 6kgs of cement. Apply over repaired part and allow to dry for 30min.  To the dry mix of 1 part by volume cement 3 parts by volume sand / aggregate add 10% to 15% of ALBOND-BL40 by weight cement diluted with equal volume of water. Mix thoroughly, then add additional water to achieve the desired consistency for towelling. The applied concrete / mortar to be cured for 72 hours by light  spray of water (mist curing) or by moist hessian bags.     <br>
                             <i> WATER PROOFING TREATMENT METHOD :</i> <br>
                            &nbsp;&nbsp;Clean the area to be water proofed thoroughly and remove all loose materials. The cracks and crevices should be filled with cement slurry containing ALBOND-BL40 adding 10% by weight of cement. Prepare bond coat of cement by mixing 1kg latex with 2 kgs of cement. Apply the bond coat of 1m thick on the prepared surface by soft brush in one direction only. Allow the coating to airdry for about 4 to 6 hours and then apply the second coat of the same mix in the direction of right angles to the first coat. Allow the second coat to dry over light and then cure for 72 hours by light spray of water (mist curing) or by laying hessian bags.
